package modelos

import (
	"strings"
	"time"

	"github.com/pkg/errors"
)

/*Publicacao representa uma publicação feita por um usuario.*/
type Publicacao struct {
	ID        uint64    `json:"id,omitempty"`
	Titulo    string    `json:"titulo,omitempty"`
	Conteudo  string    `json:"conteudo,omitempty"`
	AutorID   uint64    `json:"autorId,omitempty"`
	Autornick string    `json:"autorNick,omitempty"`
	Curtidas  uint64    `json:"curtidas"`
	CriadaEm  time.Time `json:"criadaEm,omitempty"`
}

/*Preparar faz a validação e a formatação da publicação recebida*/
func (publicacao *Publicacao) Preparar() error {
	if erro := publicacao.validar(); erro != nil {
		return erro
	}

	publicacao.formatar()
	return nil
}

func (publicacao *Publicacao) validar() error {
	if publicacao.Titulo == "" {
		return errors.New(strings.ToUpper("O titulo deve ser obrigatorio"))
	}

	if publicacao.Conteudo == "" {
		return errors.New(strings.ToUpper("O conteudo deve ser obrigatorio"))
	}

	return nil
}

func (publicacao *Publicacao) formatar() {
	publicacao.Titulo = strings.TrimSpace(publicacao.Titulo)
	publicacao.Conteudo = strings.TrimSpace(publicacao.Conteudo)
}
